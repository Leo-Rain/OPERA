t1 = (-eta_3 + eta_1);
t2 = (t1 .* rho);
t3 = E .^ 2;
t4 = (t3 .* A_1);
t8 = (2 .* C_2 .* E + B_1 + t4) .* w_1;
t9 = (xi_1 .^ 2);
t10 = (t9 .^ 2);
t15 = A_1 .* E;
t21 = A_2 .* w_1;
t22 = w_2 .* C_2;
t28 = 2 .* D_TE .* (t15 + C_2) .* w_1 + w_2 .* t4 + 2 .* E .* (t21 + t22) + B_1 .* w_2 + 2 .* B_2 .* w_1;
t33 = D_TE .^ 2;
t34 = t33 .* A_1;
t35 = w_1 .* t34;
t38 = E .* w_2 .* A_1 + t21 + t22;
t40 = w_3 .* t4;
t41 = w_2 .* A_2;
t42 = w_3 .* C_2;
t43 = t41 + t42;
t45 = B_2 .* w_2;
t47 = B_1 .* w_3;
t48 = C_3 .* w_1;
t56 = w_2 .* t34;
t59 = w_3 .* t15 + t41 + t42;
t62 = A_2 .* E .* w_3;
t63 = C_3 .* w_2;
t64 = B_2 .* w_3;
t73 = (2 .* A_2 .* D_TE + 2 .* C_3 + t34) .* w_3;
t78 = (xi_3 .^ 2);
t79 = (t78 .^ 2);
t106 = (eta_1 .^ 2);
t109 = (eta_3 .^ 2);
t111 = ((-xi_3 + xi_1) .^ 2);
t113 = sqrt((-2 .* eta_3 .* eta_1 + t106 + t109 + t111));
t116 = 0.1e1 ./ t113 .* (-(12 .* t10 .* xi_1 .* t8 .* t2) - (15 .* t10 .* rho .* t28 .* t1) - (20 .* t9 .* xi_1 .* rho .* (2 .* D_TE .* t38 + 2 .* E .* t43 + t35 + t40 + 2 .* t45 + t47 + 2 .* t48) .* t1) - 0.60e2 .* t9 .* (t56 ./ 0.2e1 + (D_TE .* t59) + t62 + t63 + t64) .* t2 - (60 .* xi_1 .* t73 .* t2) + 0.15e2 .* rho .* (0.4e1 ./ 0.5e1 .* t79 .* t8 + (t78 .* xi_3 .* t28) + t78 .* (0.4e1 ./ 0.3e1 .* t35 + 0.8e1 ./ 0.3e1 .* D_TE .* t38 + 0.4e1 ./ 0.3e1 .* t40 + 0.8e1 ./ 0.3e1 .* E .* t43 + 0.8e1 ./ 0.3e1 .* t45 + 0.4e1 ./ 0.3e1 .* t47 + 0.8e1 ./ 0.3e1 .* t48) + (xi_3 .* (4 .* D_TE .* t59 + 2 .* t56 + 4 .* t62 + 4 .* t63 + 4 .* t64)) + (4 .* t73)) .* xi_3 .* t1) ./ 0.120e3;

u_out = t116;
end